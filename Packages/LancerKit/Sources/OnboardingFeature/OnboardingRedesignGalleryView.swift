// MARK: - Step "pair" · pairing

private enum PairingMessageTone {
    case neutral
    case success
    case warning
    case error
}

private struct PairingPresentationState {
    let title: String
    let detail: String?
    let tone: PairingMessageTone
    let fieldTone: PairingMessageTone
    let showsProgress: Bool
    let disablesPrimaryAction: Bool

    init(
        pairing: E2ERelayClient.PairingState,
        connection: E2ERelayClient.ConnectionState,
        skipWarning: String?
    ) {
        if let skipWarning {
            title = skipWarning
            detail = "Pairing is still recommended. You can also pair later from Settings."
            tone = .warning
            fieldTone = .warning
            showsProgress = false
            disablesPrimaryAction = false
            return
        }

        switch pairing {
        case .paired:
            title = "Paired."
            detail = "This phone can now reach your machine over the encrypted relay."
            tone = .success
            fieldTone = .success
            showsProgress = false
            disablesPrimaryAction = false
        case .waitingForPeer:
            title = "Waiting for your desktop."
            detail = "Keep `lancerd pair` open until the relay confirms both sides."
            tone = .neutral
            fieldTone = .neutral
            showsProgress = true
            // Not disabled: if the desktop side never confirms (stale code, wrong
            // network, or — on a device that's paired before — a stale reconnect
            // attempt using old credentials) the button must stay tappable so the
            // existing warn-then-skip flow in advanceOrFinish() can run. A disabled
            // button here was a dead end with no way back to Home.
            disablesPrimaryAction = false
        case .pairingFailed(let reason):
            title = Self.failureMessage(for: reason)
            detail = Self.failureRecovery(for: reason)
            tone = .error
            fieldTone = .error
            showsProgress = false
            disablesPrimaryAction = false
        case .unpaired:
            switch connection {
            case .connecting, .reconnecting:
                title = "Connecting to the relay."
                detail = "Keep the code visible on your machine."
                tone = .neutral
                fieldTone = .neutral
                showsProgress = true
                // See the .waitingForPeer case above — stays tappable so a stuck
                // connection attempt never fully blocks the skip path.
                disablesPrimaryAction = false
            case .connected:
                title = "Enter the code from your machine."
                detail = nil
                tone = .neutral
                fieldTone = .neutral
                showsProgress = false
                disablesPrimaryAction = false
            case .disconnected:
                title = "Enter the code from your machine."
                detail = nil
                tone = .neutral
                fieldTone = .neutral
                showsProgress = false
                disablesPrimaryAction = false
            }
        }
    }

    static func failureMessage(for reason: String) -> String {
        let lower = reason.lowercased()
        if lower.contains("expired") {
            return "That code expired."
        }
        if lower.contains("mismatch") {
            return "That code didn't match."
        }
        if lower.contains("too many") || lower.contains("rate limit") {
            return "Too many attempts."
        }
        return "Couldn't connect to your machine."
    }

    static func failureRecovery(for reason: String) -> String {
        let lower = reason.lowercased()
        if lower.contains("expired") {
            return "Run `lancerd pair` again and enter the new code."
        }
        if lower.contains("mismatch") {
            return "Check the digits and try again. Your code stays here."
        }
        if lower.contains("too many") || lower.contains("rate limit") {
            return "Wait a minute, then try the same flow again."
        }
        return "Check that your machine and phone are online, then try again."
    }
}

private struct OnboardingPairingBlock: View {
    @ObservedObject var client: E2ERelayClient
    @Binding var manualCode: String
    let presentation: PairingPresentationState
    let onSubmitCode: (String) -> Void
    let canBindAccountDevice: Bool
    let deviceBindingMessage: String?
    let deviceBindingIsError: Bool
    let onBindAccountDevice: () -> Void

    @Environment(\.lancerTokens) private var t
    @FocusState private var codeFocused: Bool
    @State private var didCopyInstallCommand = false

    private static let installCommand = "curl -fsSL https://storage.googleapis.com/conduit-dist-f1c2466d/install.sh | sh"
    // Placeholder — swap for the real marketing/download page once it exists.
    private static let downloadPageURL = URL(string: "https://lancersoftware.dev/download")!

    // The pairing card assumes lancerd is already installed — a first-time user
    // has no way to get it without leaving the app. This is the one V1 escape
    // hatch: copy the real install one-liner (docs/daemon/lancerd/INSTALL.md)
    // straight to the clipboard so they can paste it into Terminal.
    private var installCommandRow: some View {
        VStack(alignment: .leading, spacing: 6) {
            Text("Don't have lancerd yet?")
                .font(.dsSansPt(11.5, weight: .semibold))
                .foregroundStyle(t.text3)
            Button {
                UIPasteboard.general.string = Self.installCommand
                Haptics.success()
                didCopyInstallCommand = true
                Task {
                    try? await Task.sleep(for: .seconds(2))
                    didCopyInstallCommand = false
                }
            } label: {
                HStack(spacing: 8) {
                    Text(Self.installCommand)
                        .font(.dsMonoPt(10.5))
                        .foregroundStyle(t.text2)
                        .lineLimit(1)
                        .truncationMode(.middle)
                    Spacer(minLength: 8)
                    DSIconView(didCopyInstallCommand ? .check : .copy, size: 13, color: t.text3)
                }
                .padding(.horizontal, 10)
                .padding(.vertical, 8)
                .background(t.surface, in: RoundedRectangle(cornerRadius: 10, style: .continuous))
                .overlay(RoundedRectangle(cornerRadius: 10, style: .continuous).strokeBorder(t.border, lineWidth: 1))
            }
            .buttonStyle(.plain)
            .accessibilityLabel("Copy lancerd install command")
            Text(didCopyInstallCommand ? "Copied — paste into Terminal on your machine." : "Copies a one-line installer. Runs on macOS or Linux.")
                .font(.dsSansPt(10.5))
                .foregroundStyle(t.text4)
            DSLink("or download from the website", size: 10.5) {
                UIApplication.shared.open(Self.downloadPageURL)
            }
        }
        .padding(.top, 4)
    }

    var body: some View {
        VStack(alignment: .leading, spacing: 16) {
            VStack(alignment: .leading, spacing: 10) {
                Text("ON YOUR MACHINE")
                    .font(.dsMonoPt(10, weight: .medium))
                    .tracking(1.2)
                    .foregroundStyle(t.text4)

                Text("Run `lancerd pair`, then enter the 6-digit code it prints.")
                    .font(.dsSansPt(13, weight: .medium))
                    .foregroundStyle(t.text2)
                    .fixedSize(horizontal: false, vertical: true)

                TextField("000000", text: $manualCode)
                    .keyboardType(.numberPad)
                    .font(.dsDisplayPt(26, weight: .bold))
                    .multilineTextAlignment(.center)
                    .tracking(8)
                    .focused($codeFocused)
                    .onChange(of: manualCode) { _, v in
                        let digits = String(v.filter(\.isNumber).prefix(6))
                        if digits != v { manualCode = digits }
                        if digits.count == 6 { codeFocused = false; onSubmitCode(digits) }
                    }
                    .padding(.vertical, 12)
                    .frame(maxWidth: .infinity)
                    .background(fieldFill, in: RoundedRectangle(cornerRadius: 14, style: .continuous))
                    .overlay(RoundedRectangle(cornerRadius: 14, style: .continuous).strokeBorder(fieldBorder, lineWidth: 1.5))
                    .accessibilityIdentifier("pairingCodeField")

                inlineStatus

                Text("Codes expire in a few minutes, work once, and should not be shared.")
                    .font(.dsSansPt(11.5))
                    .foregroundStyle(t.text4)
                    .fixedSize(horizontal: false, vertical: true)

                installCommandRow
            }
            .padding(14)
            .background(t.surface2, in: RoundedRectangle(cornerRadius: 18, style: .continuous))
            .overlay(RoundedRectangle(cornerRadius: 18, style: .continuous).strokeBorder(fieldGroupBorder, lineWidth: 1))

            if canBindAccountDevice {
                Button(action: onBindAccountDevice) {
                    Label("link this machine to my account", systemImage: "qrcode.viewfinder")
                        .font(.dsSansPt(13, weight: .semibold))
                        .foregroundStyle(t.accent)
                        .frame(maxWidth: .infinity)
                        .padding(.top, 8)
                }
                .buttonStyle(.plain)
                .accessibilityIdentifier("accountDeviceBinding")
                Text("Scans a one-time security code. Lancer never asks for your account password on this machine.")
                    .font(.dsSansPt(11.5))
                    .foregroundStyle(t.text4)
                    .multilineTextAlignment(.center)
                    .frame(maxWidth: .infinity)
                if let deviceBindingMessage {
                    Text(deviceBindingMessage)
                        .font(.dsSansPt(12, weight: .medium))
                        .foregroundStyle(deviceBindingIsError ? t.danger : t.ok)
                        .multilineTextAlignment(.center)
                        .frame(maxWidth: .infinity)
                }
            }
        }
        .onChange(of: client.pairingState) { _, state in
            switch state {
            case .paired:        Haptics.success()
            case .pairingFailed: Haptics.error()
            case .unpaired, .waitingForPeer: break
            }
        }
    }

    @ViewBuilder
    private var inlineStatus: some View {
        HStack(alignment: .top, spacing: 8) {
            if presentation.showsProgress {
                ProgressView()
                    .controlSize(.small)
                    .padding(.top, 1)
            } else {
                Image(systemName: statusSymbol)
                    .font(.system(size: 12, weight: .bold))
                    .foregroundStyle(statusColor)
                    .padding(.top, 2)
            }
            VStack(alignment: .leading, spacing: 2) {
                Text(presentation.title)
                    .font(.dsSansPt(12.5, weight: .semibold))
                    .foregroundStyle(statusColor)
                    .fixedSize(horizontal: false, vertical: true)
                if let detail = presentation.detail {
                    Text(detail)
                        .font(.dsSansPt(11.5))
                        .foregroundStyle(t.text3)
                        .fixedSize(horizontal: false, vertical: true)
                }
            }
            Spacer(minLength: 0)
        }
    }

    private var statusSymbol: String {
        switch presentation.tone {
        case .success: return "checkmark.circle.fill"
        case .warning: return "exclamationmark.triangle.fill"
        case .error: return "xmark.circle.fill"
        case .neutral: return "info.circle.fill"
        }
    }

    private var statusColor: Color {
        switch presentation.tone {
        case .success: return t.ok
        case .warning: return t.warn
        case .error: return t.danger
        case .neutral: return t.text4
        }
    }

    private var fieldBorder: Color {
        if codeFocused { return t.accent }
        switch presentation.fieldTone {
        case .success: return t.ok
        case .warning: return t.warn
        case .error: return t.danger
        case .neutral: return t.border
        }
    }

    private var fieldFill: Color {
        switch presentation.fieldTone {
        case .success: return t.okSoft
        case .warning: return t.warnSoft
        case .error: return t.dangerSoft
        case .neutral: return t.surface
        }
    }

    private var fieldGroupBorder: Color {
        switch presentation.fieldTone {
        case .success: return t.ok.opacity(0.5)
        case .warning: return t.warn.opacity(0.55)
        case .error: return t.danger.opacity(0.6)
        case .neutral: return t.border
        }
    }
}

