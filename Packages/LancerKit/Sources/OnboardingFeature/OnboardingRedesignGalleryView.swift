// MARK: - OnboardingRedesignView (productized 3-step first-run)

/// First-run onboarding in the simplified 3-step language: Why Lancer → Pair the
/// bridge → Default policy. Pairing is real (drives the app-wide `E2ERelayClient`
/// with a real 6-digit code); the chosen policy tier is persisted and pushed to the
/// daemon on first connect via `OnboardingPolicy`.
///
/// Visuals are a faithful reproduction of the `Lancer App.dc.html` onboarding board:
/// a terracotta editorial hero (grid texture + lavender pixel mark, Instrument Serif
/// italic kicker over a Bricolage display title), then a per-step block — value rows,
/// pairing digit-boxes, or policy cards — with a black/orange CTA and an always-present
/// "I've already set up Lancer" link.
public struct OnboardingRedesignView: View {
    let onContinue: () -> Void
    let onAlreadyUseLancer: () -> Void
    let onSetupWorkspace: () -> Void
    /// Optional: finish onboarding and route the user to "Add a machine" (SSH).
    /// Provided by the app shell; when nil the SSH step's primary action just
    /// finishes onboarding like Skip.
    let onEnableSSH: (() -> Void)?

    @StateObject private var client: E2ERelayClient
    private let accountSession: AccountSessionController?
    @State private var step: Int
    @State private var selectedLevel: OnboardingCautionLevel = .balanced
    // Relay pairing: the code is generated by the DESKTOP (`lancerd pair`) and
    // either scanned from its QR or typed in here — the phone never invents its own.
    @State private var manualCode = ""
    @State private var showPairingScanner = false
    @State private var pairingMessage: String?
    @State private var showDeviceBindingScanner = false
    @State private var deviceBindingMessage: String?
    @State private var deviceBindingIsError = false

    @AppStorage("lancer.onboarding.autonomyPreset") private var storedPreset: String = ""
    @Environment(\.lancerTokens) private var t
    @Environment(\.accessibilityReduceMotion) private var reduceMotion

    private let steps = OnboardingRedesignStep.all

    public init(
        onContinue: @escaping () -> Void,
        onAlreadyUseLancer: @escaping () -> Void = {},
        onSetupWorkspace: @escaping () -> Void = {},
        onEnableSSH: (() -> Void)? = nil,
        relayClient: E2ERelayClient? = nil,
        accountSession: AccountSessionController? = nil,
        startStep: Int = 0
    ) {
        self.onContinue = onContinue
        self.onAlreadyUseLancer = onAlreadyUseLancer
        self.onSetupWorkspace = onSetupWorkspace
        self.onEnableSSH = onEnableSSH
        self.accountSession = accountSession
        let resolved = relayClient ?? E2ERelayClient(
            relayURL: RelaySettings.url(),
            pairingCode: PairingCrypto.generatePairingCode()
        )
        _client = StateObject(wrappedValue: resolved)
        _step = State(initialValue: min(max(startStep, 0), OnboardingRedesignStep.all.count - 1))
    }

    private var current: OnboardingRedesignStep { steps[step] }

    public var body: some View {
        Group {
            // Start with the product value. Account creation follows the first
            // screen so people understand why Lancer asks for identity details.
            if current.kind != .value, let accountSession, accountSession.mode == nil {
                AccountEntryView(account: accountSession, onComplete: advanceAfterAccount)
            } else if current.kind == .sshSetup {
                // The optional SSH step renders its own header + CTAs (no shared
                // hero/footer), so present it standalone like the account gate.
                OnboardingSSHSetupScreen(
                    onAddHost: { finishOnboarding(routeToAddHost: true) },
                    onSkip: { finishOnboarding(routeToAddHost: false) }
                )
            } else {
                VStack(spacing: 0) {
                    hero
                    ScrollView(.vertical, showsIndicators: false) {
                        primaryBlock
                            .frame(maxWidth: 560, alignment: .leading)
                            .frame(maxWidth: .infinity, alignment: .leading)
                            .padding(.top, 24)
                            .padding(.bottom, 24)
                    }
                    footer
                }
            }
        }
        .background(t.bg.ignoresSafeArea())
        .dynamicTypeSize(...DynamicTypeSize.accessibility3)
        // Relay pairing: scan the desktop's `lancerd pair` QR, or type its code.
        .sheet(isPresented: $showPairingScanner) {
            OnboardingScanScreen(
                onScan: { payload in
                    showPairingScanner = false
                    if let pairing = OnboardingPairing.extractPairing(fromScanned: payload) {
                        pairingMessage = nil
                        applyPairingCode(pairing.code, relay: pairing.relay)
                    } else {
                        pairingMessage = "That QR isn't a Lancer pairing code. Run `lancerd pair` on your machine."
                        Haptics.error()
                    }
                },
                onUnavailable: { reason in
                    showPairingScanner = false
                    // Simulator/no-camera: fall back to the manual 6-digit field.
                    pairingMessage = reason
                },
                onEnterCodeInstead: { showPairingScanner = false }
            )
        }
        .sheet(isPresented: $showDeviceBindingScanner) {
            OnboardingScanScreen(
                onScan: { payload in
                    showDeviceBindingScanner = false
                    bindAccountDevice(scannedPayload: payload)
                },
                onUnavailable: { reason in
                    showDeviceBindingScanner = false
                    deviceBindingMessage = reason
                    deviceBindingIsError = true
                    Haptics.warning()
                },
                onEnterCodeInstead: {
                    showDeviceBindingScanner = false
                    deviceBindingMessage = "Use the QR scanner on a physical device to bind a daemon."
                    deviceBindingIsError = true
                }
            )
        }
    }

    // MARK: Hero (terracotta editorial header — shared across steps)

    private var hero: some View {
        VStack(alignment: .leading, spacing: 0) {
            headerRow
                .padding(.horizontal, 26)
                .padding(.top, 6)

            VStack(alignment: .leading, spacing: 0) {
                OnboardingBrandMark()
                    .padding(.bottom, 18)
                Text(current.eyebrow)
                    .font(.dsEditorialPt(20))
                    .foregroundStyle(OnboardingPalette.heroKicker)
                Text(current.title)
                    .font(.dsDisplayPt(34, weight: .heavy))
                    .tracking(-1)
                    .foregroundStyle(.white)
                    .lineLimit(2)
                    .minimumScaleFactor(0.85)
                    .fixedSize(horizontal: false, vertical: true)
                    .padding(.top, 5)
                Text(current.body)
                    .font(.dsSansPt(13))
                    .lineSpacing(3)
                    .foregroundStyle(.white.opacity(0.88))
                    .fixedSize(horizontal: false, vertical: true)
                    .frame(maxWidth: 292, alignment: .leading)
                    .padding(.top, 12)
            }
            .padding(.horizontal, 28)
            .padding(.top, 22)
        }
        .padding(.bottom, 28)
        .frame(maxWidth: .infinity, alignment: .leading)
        .background(heroBackground.ignoresSafeArea(edges: .top))
    }

    private var heroBackground: some View {
        ZStack(alignment: .bottomTrailing) {
            LinearGradient(colors: [t.accent, t.accentInk], startPoint: .topLeading, endPoint: .bottomTrailing)
            Canvas { ctx, size in
                var y: CGFloat = 0
                while y <= size.height {
                    ctx.fill(Path(CGRect(x: 0, y: y, width: size.width, height: 1)),
                             with: .color(.white.opacity(0.05)))
                    y += 30
                }
            }
            Circle()
                .fill(.white.opacity(0.07))
                .frame(width: 190, height: 190)
                .offset(x: 34, y: 46)
        }
        .clipShape(UnevenRoundedRectangle(bottomLeadingRadius: 34, bottomTrailingRadius: 34, style: .continuous))
    }

    private var headerRow: some View {
        HStack(spacing: 9) {
            Button {
                guard step > 0 else { return }
                withAnimation(LancerMotion.resolved(.smooth(duration: 0.28, extraBounce: 0), reduceMotion: reduceMotion)) { step -= 1 }
            } label: {
                Image(systemName: "chevron.left")
                    .font(.system(size: 18, weight: .semibold))
                    .foregroundStyle(.white)
                    .frame(width: 14)
            }
            .buttonStyle(.plain)
            .opacity(step > 0 ? 1 : 0)
            .disabled(step == 0)
            .accessibilityLabel("Back")
            .accessibilityIdentifier("onboardingBack")

            ForEach(0..<steps.count, id: \.self) { i in
                Capsule(style: .continuous)
                    .fill(i <= step ? Color.white : Color.white.opacity(0.4))
                    .frame(width: i == step ? 22 : 7, height: 7)
                    .animation(reduceMotion ? nil : .easeOut(duration: 0.3), value: step)
            }

            Spacer(minLength: 0)
        }
        .frame(height: 20)
    }

    // MARK: Per-step primary block

    @ViewBuilder
    private var primaryBlock: some View {
        switch current.kind {
        case .value:
            OnboardingValueRows().padding(.horizontal, 28).padding(.top, 12)
        case .pair:
            OnboardingPairingBlock(
                client: client,
                manualCode: $manualCode,
                pairingMessage: pairingMessage,
                onScan: { showPairingScanner = true },
                onSubmitCode: { code in applyPairingCode(code, relay: nil) },
                canBindAccountDevice: accountSession?.isStandardAccount == true,
                deviceBindingMessage: deviceBindingMessage,
                deviceBindingIsError: deviceBindingIsError,
                onBindAccountDevice: { showDeviceBindingScanner = true }
            )
                .padding(.horizontal, 24)
        case .policy:
            OnboardingPolicyCards(selectedLevel: $selectedLevel).padding(.horizontal, 24)
        case .sshSetup:
            // Rendered standalone from `body` (its own chrome); never shown here.
            EmptyView()
        }
    }

    // MARK: Footer CTA

    private var footer: some View {
        VStack(spacing: 11) {
            DSButton(
                current.ctaArrow ? "\(current.primaryAction) →" : current.primaryAction,
                variant: .primary,
                size: .lg,
                fullWidth: true,
                action: advanceOrFinish
            )
            .accessibilityIdentifier("onboardingPrimary")
        }
        .padding(.horizontal, 24)
        .padding(.top, 14)
        .padding(.bottom, 16)
        .background(t.bg.ignoresSafeArea(edges: .bottom))
    }

    // MARK: Actions

    private func advanceOrFinish() {
        if step < steps.count - 1 {
            Haptics.selection()
            withAnimation(LancerMotion.resolved(.smooth(duration: 0.28, extraBounce: 0), reduceMotion: reduceMotion)) { step += 1 }
        } else {
            finish()
        }
    }

    private func advanceAfterAccount() {
        Haptics.success()
        // AccountEntry only appears after the value screen. Keep the pair step
        // selected rather than requiring an extra, anonymous onboarding state.
        if step == 0 {
            step = min(1, steps.count - 1)
        }
    }

    /// Connect to the relay using the DESKTOP's pairing code (from a scanned QR or
    /// typed in). Starts a fresh keypair, adopts the QR's relay URL when present
    /// (self-hosters) else the shipped relay, then connects. `connect()` is
    /// idempotent, so re-entry (rescan / retype) cleanly replaces the channel.
    private func applyPairingCode(_ code: String, relay: URL?) {
        guard code.count == 6 else { return }
        client.beginPairingSession()        // fresh keypair for this attempt
        client.relayURL = relay ?? RelaySettings.url()
        client.pairingCode = code
        client.connect()
        Haptics.selection()
    }

    private func finish() {
        finishOnboarding(routeToAddHost: false)
    }

    /// Persist the chosen policy tier and leave onboarding. When `routeToAddHost`
    /// and the app shell supplied `onEnableSSH`, route the user straight to "Add a
    /// machine"; otherwise just enter the app.
    private func finishOnboarding(routeToAddHost: Bool) {
        storedPreset = selectedLevel.mappedPreset.rawValue
        // Push this tier's starter policy on the first daemon connect (see OnboardingPolicy).
        OnboardingPolicy.markPending(selectedLevel)
        Haptics.success()
        if routeToAddHost, let onEnableSSH { onEnableSSH() } else { onContinue() }
    }

    private func bindAccountDevice(scannedPayload: String) {
        guard let challenge = OnboardingPairing.extractDeviceBinding(fromScanned: scannedPayload) else {
            deviceBindingMessage = "That QR code can pair the relay, but it is not an account device-binding challenge."
            deviceBindingIsError = true
            Haptics.error()
            return
        }
        guard let accountSession else { return }
        Task {
            do {
                try await accountSession.bindDaemonDevice(
                    challengeID: challenge.challengeID,
                    secret: challenge.secret,
                    backendURL: challenge.backendURL
                )
                deviceBindingMessage = "Device authorized. The daemon will finish binding without your password."
                deviceBindingIsError = false
                Haptics.success()
            } catch {
                deviceBindingMessage = "Couldn't authorize this device. Check the account session and try again."
                deviceBindingIsError = true
                Haptics.error()
            }
        }
    }
}

