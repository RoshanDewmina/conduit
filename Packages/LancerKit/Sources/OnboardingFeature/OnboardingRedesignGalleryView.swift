// MARK: - OnboardingRedesignView (productized 3-step first-run)

/// First-run onboarding in the simplified 3-step language: Why Lancer → Pair the
/// bridge → Default policy. Pairing is real (drives the app-wide `E2ERelayClient`
/// with a real 6-digit code); the chosen policy tier is persisted and pushed to the
/// daemon on first connect via `OnboardingPolicy`.
///
/// Visuals follow the editorial onboarding board, but the first screen now proves
/// the actual product loop before asking the user to pair a machine.
public struct OnboardingRedesignView: View {
    let onContinue: () -> Void
    let onPaired: ((E2ERelayClient, RelayMachineRecord) -> Void)?

    @StateObject private var client: E2ERelayClient
    private let accountSession: AccountSessionController?
    @State private var step: Int
    @State private var selectedLevel: OnboardingCautionLevel = .balanced
    // Relay pairing: the code is generated by the DESKTOP (`lancerd pair`) and
    // typed in here — the phone never invents its own. Code-only: no QR scan path.
    @State private var manualCode = ""
    @State private var pairingMessage: String?
    @State private var showDeviceBindingScanner = false
    @State private var deviceBindingMessage: String?
    @State private var deviceBindingIsError = false

    @AppStorage("lancer.onboarding.autonomyPreset") private var storedPreset: String = ""
    @Environment(\.lancerTokens) private var t
    @Environment(\.accessibilityReduceMotion) private var reduceMotion

    private let steps = OnboardingRedesignStep.all

    public init(
        onContinue: @escaping () -> Void,
        accountSession: AccountSessionController? = nil,
        startStep: Int = 0,
        onPaired: ((E2ERelayClient, RelayMachineRecord) -> Void)? = nil
    ) {
        self.onContinue = onContinue
        self.accountSession = accountSession
        self.onPaired = onPaired
        let resolved = E2ERelayClient(
            relayURL: RelaySettings.url(),
            pairingCode: PairingCrypto.generatePairingCode()
        )
        _client = StateObject(wrappedValue: resolved)
        _step = State(initialValue: min(max(startStep, 0), OnboardingRedesignStep.all.count - 1))
    }

    private var current: OnboardingRedesignStep { steps[step] }

    private var pairingPresentation: PairingPresentationState {
        PairingPresentationState(
            pairing: client.pairingState,
            connection: client.connectionState,
            skipWarning: pairingMessage
        )
    }

    /// True while the account-creation gate is showing instead of the current step's
    /// own content. Computed once and reused by both `content` and `footer` below so
    /// the two can never disagree about which branch is active.
    private var isAccountGateActive: Bool {
        current.kind != .value && current.kind != .pair && accountSession != nil && accountSession?.mode == nil
    }

    public var body: some View {
        // Every onboarding screen — carousel steps, the account gate, SSH setup — renders
        // through the same scaffold so the back chevron and step dots never disappear or
        // shift between screens (previously the account gate and SSH setup each drew their
        // own ad-hoc header with no back affordance at all).
        OnboardingScaffold(
            stepIndex: step,
            totalSteps: steps.count,
            leading: step > 0 ? .back : .none,
            topBarStyle: current.kind == .value ? .hero : .page,
            onLeading: {
                guard step > 0 else { return }
                withAnimation(LancerMotion.resolved(.smooth(duration: 0.28, extraBounce: 0), reduceMotion: reduceMotion)) { step -= 1 }
            }
        ) {
            // Account creation gates everything after the relay pairing step.
            if isAccountGateActive, let accountSession {
                AccountEntryView(account: accountSession, onComplete: advanceAfterAccount)
            } else {
                ScrollView(.vertical, showsIndicators: false) {
                    VStack(alignment: .leading, spacing: 0) {
                        // Full-bleed — NOT inside the 560pt content constraint below, or
                        // the hero stops spanning the full width on iPad/wide screens.
                        OnboardingHeroBanner(
                            eyebrow: current.eyebrow,
                            title: current.title,
                            subtitle: current.body,
                            compact: current.kind == .value || current.kind == .pair
                        )
                        primaryBlock
                            .frame(maxWidth: 560, alignment: .leading)
                            .frame(maxWidth: .infinity, alignment: .leading)
                            .padding(.top, 24)
                            .padding(.bottom, 24)
                    }
                }
            }
        } footer: {
            // The account gate renders its own CTA inline as content (sign-in/sign-up
            // buttons) — only the carousel steps use the scaffold's dedicated footer slot.
            if !isAccountGateActive {
                OnboardingFooter {
                    VStack(spacing: 10) {
                        DSButton(
                            current.ctaArrow ? "\(current.primaryAction) →" : current.primaryAction,
                            variant: .primary, size: .lg, fullWidth: true,
                            action: advanceOrFinish
                        )
                        .disabled(current.kind == .pair && pairingPresentation.disablesPrimaryAction)
                        .accessibilityIdentifier("onboardingPrimary")

                        // A real, always-tappable skip — not just the warn-then-tap-again
                        // path on the primary button above, which isn't discoverable.
                        if current.kind == .pair, client.pairingState != .paired {
                            Button("Skip for now", action: skipPairing)
                                .font(.dsSansPt(13, weight: .medium))
                                .foregroundStyle(t.text3)
                                .accessibilityIdentifier("onboardingSkipPairing")
                        }
                    }
                }
            }
        }
        .dynamicTypeSize(...DynamicTypeSize.accessibility3)
        .onChange(of: client.pairingState) { _, state in
            guard state == .paired, let onPaired else { return }
            onPaired(client, RelayMachineRecord(id: client.machineID, displayName: "Relay host", pairedAt: .now))
        }
        .sheet(isPresented: $showDeviceBindingScanner) {
            OnboardingScanScreen(
                onScan: { payload in
                    showDeviceBindingScanner = false
                    bindAccountDevice(scannedPayload: payload)
                },
                onUnavailable: { reason in
                    showDeviceBindingScanner = false
                    deviceBindingMessage = reason
                    deviceBindingIsError = true
                    Haptics.warning()
                },
                onEnterCodeInstead: {
                    showDeviceBindingScanner = false
                    deviceBindingMessage = "Use the QR scanner on a physical device to bind a daemon."
                    deviceBindingIsError = true
                }
            )
        }
    }


    // MARK: Per-step primary block

    @ViewBuilder
    private var primaryBlock: some View {
        switch current.kind {
        case .value:
            VStack(alignment: .leading, spacing: 0) {
                OnboardingProductProofPreview()
                    .padding(.horizontal, 24)
                    .padding(.top, 2)
            }
        case .pair:
            VStack(alignment: .leading, spacing: 18) {
                OnboardingPairingBlock(
                    client: client,
                    manualCode: $manualCode,
                    presentation: pairingPresentation,
                    onSubmitCode: { code in applyPairingCode(code, relay: nil) },
                    canBindAccountDevice: accountSession?.isStandardAccount == true,
                    deviceBindingMessage: deviceBindingMessage,
                    deviceBindingIsError: deviceBindingIsError,
                    onBindAccountDevice: { showDeviceBindingScanner = true }
                )
                    .padding(.horizontal, 24)
            }
        case .policy:
            OnboardingPolicyCards(selectedLevel: $selectedLevel).padding(.horizontal, 24)
        }
    }

    // MARK: Actions

    private func advanceOrFinish() {
        // "Pair & continue" previously advanced unconditionally, so a user could
        // finish onboarding having never actually paired a machine — landing on an
        // empty Home with no way back to the code field. First tap while unpaired
        // shows a real warning (reusing `pairingMessage`, previously wired but never
        // set) instead of silently proceeding; a second tap explicitly skips.
        if current.kind == .pair, client.pairingState != .paired, pairingMessage == nil {
            pairingMessage = "No machine paired yet. Tap Pair & continue again to skip for now — you can pair later from Settings."
            Haptics.warning()
            return
        }
        if step < steps.count - 1 {
            Haptics.selection()
            withAnimation(LancerMotion.resolved(.smooth(duration: 0.28, extraBounce: 0), reduceMotion: reduceMotion)) { step += 1 }
        } else {
            finish()
        }
    }

    /// A dedicated, always-visible "Skip for now" affordance for the pairing step —
    /// tapping it already states the intent, so unlike `advanceOrFinish` it doesn't
    /// need the warn-then-repeat-tap pattern first.
    private func skipPairing() {
        Haptics.selection()
        pairingMessage = nil
        if step < steps.count - 1 {
            withAnimation(LancerMotion.resolved(.smooth(duration: 0.28, extraBounce: 0), reduceMotion: reduceMotion)) { step += 1 }
        } else {
            finish()
        }
    }

    private func advanceAfterAccount() {
        Haptics.success()
        // AccountEntry only appears once the user tries to move past relay pairing.
        // Defensive no-op in the normal flow since step
        // is already wherever the user was heading; guards a fresh/resumed session
        // that could otherwise land here with step still at 0.
        if step < 2 {
            step = min(2, steps.count - 1)
        }
    }

    /// Connect to the relay using the DESKTOP's pairing code, typed in here. Starts
    /// a fresh keypair, then connects. `connect()` is idempotent, so re-entry
    /// (retype) cleanly replaces the channel.
    private func applyPairingCode(_ code: String, relay: URL?) {
        guard code.count == 6 else { return }
        pairingMessage = nil                // clear any stale skip-warning
        client.beginPairingSession()        // fresh keypair for this attempt
        client.relayURL = relay ?? RelaySettings.url()
        client.pairingCode = code
        client.connect()
        Haptics.selection()
    }

    private func finish() {
        finishOnboarding()
    }

    /// Persist the chosen policy tier and leave onboarding.
    private func finishOnboarding() {
        storedPreset = selectedLevel.mappedPreset.rawValue
        // Push this tier's starter policy on the first daemon connect (see OnboardingPolicy).
        OnboardingPolicy.markPending(selectedLevel)
        Haptics.success()
        onContinue()
    }

    private func bindAccountDevice(scannedPayload: String) {
        guard let challenge = OnboardingPairing.extractDeviceBinding(fromScanned: scannedPayload) else {
            deviceBindingMessage = "That QR code can pair the relay, but it is not an account device-binding challenge."
            deviceBindingIsError = true
            Haptics.error()
            return
        }
        guard let accountSession else { return }
        Task {
            do {
                try await accountSession.bindDaemonDevice(
                    challengeID: challenge.challengeID,
                    secret: challenge.secret,
                    backendURL: challenge.backendURL
                )
                deviceBindingMessage = "Device authorized. The daemon will finish binding without your password."
                deviceBindingIsError = false
                Haptics.success()
            } catch {
                deviceBindingMessage = "Couldn't authorize this device. Check the account session and try again."
                deviceBindingIsError = true
                Haptics.error()
            }
        }
    }
}

