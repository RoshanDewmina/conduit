// MARK: - Step 1 · pairing

private struct OnboardingPairingBlock: View {
    @ObservedObject var client: E2ERelayClient
    @Binding var manualCode: String
    let pairingMessage: String?
    let onScan: () -> Void
    let onSubmitCode: (String) -> Void
    let canBindAccountDevice: Bool
    let deviceBindingMessage: String?
    let deviceBindingIsError: Bool
    let onBindAccountDevice: () -> Void

    @Environment(\.lancerTokens) private var t
    @FocusState private var codeFocused: Bool

    var body: some View {
        VStack(alignment: .leading, spacing: 16) {
            // On your desktop, run `lancerd pair` — it prints a QR + 6-digit code.
            Text("ON YOUR DESKTOP, RUN  lancerd pair")
                .font(.dsMonoPt(10, weight: .medium))
                .tracking(1.2)
                .foregroundStyle(t.text4)

            // Primary: scan the desktop's QR (camera; device only).
            Button(action: onScan) {
                HStack(spacing: 12) {
                    Image(systemName: "qrcode.viewfinder")
                        .font(.system(size: 22, weight: .semibold))
                        .foregroundStyle(t.accent)
                        .frame(width: 30)
                    VStack(alignment: .leading, spacing: 2) {
                        Text("Scan the QR on your desktop")
                            .font(.dsSansPt(15, weight: .semibold)).foregroundStyle(t.text)
                        Text("Point your camera at the code in your terminal")
                            .font(.dsSansPt(12)).foregroundStyle(t.text4)
                    }
                    Spacer(minLength: 0)
                    Image(systemName: "chevron.right").font(.system(size: 12, weight: .semibold)).foregroundStyle(t.text4)
                }
                .padding(16)
                .frame(maxWidth: .infinity, alignment: .leading)
                .background(t.surface, in: RoundedRectangle(cornerRadius: 16, style: .continuous))
                .overlay(RoundedRectangle(cornerRadius: 16, style: .continuous).strokeBorder(t.accent.opacity(0.5), lineWidth: 1))
            }
            .buttonStyle(.plain)
            .accessibilityIdentifier("pairingScan")

            // Divider → manual entry.
            HStack(spacing: 10) {
                Rectangle().fill(t.border).frame(height: 1)
                Text("or enter the 6-digit code").font(.dsSansPt(12)).foregroundStyle(t.text4).fixedSize()
                Rectangle().fill(t.border).frame(height: 1)
            }

            TextField("000000", text: $manualCode)
                .keyboardType(.numberPad)
                .font(.dsDisplayPt(26, weight: .bold))
                .multilineTextAlignment(.center)
                .tracking(8)
                .focused($codeFocused)
                .onChange(of: manualCode) { _, v in
                    let digits = String(v.filter(\.isNumber).prefix(6))
                    if digits != v { manualCode = digits }
                    if digits.count == 6 { codeFocused = false; onSubmitCode(digits) }
                }
                .padding(.vertical, 16)
                .frame(maxWidth: .infinity)
                .background(t.surface, in: RoundedRectangle(cornerRadius: 14, style: .continuous))
                .overlay(RoundedRectangle(cornerRadius: 14, style: .continuous).strokeBorder(codeFocused ? t.accent : t.border, lineWidth: 1.5))
                .accessibilityIdentifier("pairingCodeField")

            Text(statusLabel)
                .font(.dsSansPt(12))
                .foregroundStyle(isPaired ? t.accent : t.text4)
                .frame(maxWidth: .infinity, alignment: .center)

            if let pairingMessage {
                Text(pairingMessage)
                    .font(.dsSansPt(12, weight: .medium))
                    .foregroundStyle(t.danger)
                    .multilineTextAlignment(.center)
                    .frame(maxWidth: .infinity)
            }

            if canBindAccountDevice {
                Button(action: onBindAccountDevice) {
                    Label("bind this daemon to my account", systemImage: "qrcode.viewfinder")
                        .font(.dsSansPt(13, weight: .semibold))
                        .foregroundStyle(t.accent)
                        .frame(maxWidth: .infinity)
                        .padding(.top, 8)
                }
                .buttonStyle(.plain)
                .accessibilityIdentifier("accountDeviceBinding")
                Text("Scans a one-time daemon challenge. Lancer never asks for your account password on the host.")
                    .font(.dsSansPt(11.5))
                    .foregroundStyle(t.text4)
                    .multilineTextAlignment(.center)
                    .frame(maxWidth: .infinity)
                if let deviceBindingMessage {
                    Text(deviceBindingMessage)
                        .font(.dsSansPt(12, weight: .medium))
                        .foregroundStyle(deviceBindingIsError ? t.danger : t.ok)
                        .multilineTextAlignment(.center)
                        .frame(maxWidth: .infinity)
                }
            }
        }
        .onChange(of: client.pairingState) { _, state in
            switch state {
            case .paired:        Haptics.success()
            case .pairingFailed: Haptics.error()
            case .unpaired, .waitingForPeer: break
            }
        }
    }

    private var isPaired: Bool { client.pairingState == .paired }

    private var statusLabel: String {
        switch client.pairingState {
        case .unpaired:        return client.connectionState == .disconnected ? "Enter or scan your desktop code to pair" : "Connecting…"
        case .waitingForPeer:  return "Waiting for your desktop…"
        case .paired:          return "Paired ✓"
        case .pairingFailed:   return "Pairing failed — re-scan or re-enter the code"
        }
    }
}

// MARK: - Step 2 · policy cards

private struct OnboardingPolicyCards: View {
    @Binding var selectedLevel: OnboardingCautionLevel

    @Environment(\.lancerTokens) private var t
    @Environment(\.accessibilityReduceMotion) private var reduceMotion

    var body: some View {
        VStack(spacing: 11) {
            ForEach(OnboardingCautionLevel.allCases) { level in
                let isSelected = selectedLevel == level
                Button {
                    Haptics.selection()
                    withAnimation(LancerMotion.resolved(.smooth(duration: 0.18, extraBounce: 0), reduceMotion: reduceMotion)) {
                        selectedLevel = level
                    }
                } label: {
                    HStack(spacing: 13) {
                        ZStack {
                            Circle()
                                .strokeBorder(isSelected ? t.accent : t.borderStrong, lineWidth: 2)
                                .frame(width: 24, height: 24)
                            Circle()
                                .fill(isSelected ? t.accent : .clear)
                                .frame(width: 9, height: 9)
                        }
                        VStack(alignment: .leading, spacing: 1) {
                            Text(level.title).font(.dsDisplayPt(16, weight: .bold)).foregroundStyle(t.text)
                            Text(level.detail).font(.dsSansPt(12.5)).foregroundStyle(t.text4)
                                .fixedSize(horizontal: false, vertical: true)
                        }
                        Spacer(minLength: 0)
                    }
                    .padding(15)
                    .frame(maxWidth: .infinity, alignment: .leading)
                    .background(
                        RoundedRectangle(cornerRadius: 16, style: .continuous).fill(t.surface)
                    )
                    .overlay(
                        RoundedRectangle(cornerRadius: 16, style: .continuous)
                            .strokeBorder(isSelected ? t.accent : t.border, lineWidth: 1.5)
                    )
                    .shadow(color: .black.opacity(isSelected ? 0.16 : 0.05), radius: isSelected ? 14 : 3, x: 0, y: isSelected ? 6 : 1)
                }
                .buttonStyle(.plain)
                .accessibilityIdentifier("policyPreset_\(level.rawValue)")
                .accessibilityValue(isSelected ? "selected" : "unselected")
            }
        }
    }
}

